//!
//! \file     status_report_observer.h
//! \brief    Defines the class for status report observer
//! \details  
//!
#ifndef __MEDIA_STATUS_REPORT_OBSERVER_H__
#define __MEDIA_STATUS_REPORT_OBSERVER_H__

#include "mos_defs.h"

class MediaStatusReportObserver
{
public:
    virtual ~MediaStatusReportObserver() {};
    //!
    //! \brief  Notify the observers that one frame is completed
    //! \param  [in] mfxStatus
    //!         pointer to status buffer which for MFX
    //! \param  [in] rcsStatus
    //!         pointer to status buffer which for RCS
    //! \param  [in, out] statusReport
    //!         pointer of EncoderStatusReport
    //! \return MOS_STATUS
    //!         MOS_STATUS_SUCCESS if success, else fail reason
    //!
    virtual MOS_STATUS Completed(void *mfxStatus, void *rcsStatus, void *statusReport) = 0;
};

#endif // !__MEDIA_STATUS_REPORT_OBSERVER_H__
